class Parser
{
    static public function parse($data, $tabsize = 4)
    {
        $tokens = token_get_all($data);

        // Order is important!
        $it = new Parser\TokenIterator($tokens, $tabsize);
        $it = new Parser\DetectKeywordsTokenIterator($it);

        $php = Parser\Transform::transform($it);
        return $php;
    }
}
